      <h2 class="subtitle">   Leur idéaux</h2>
« Une guitare et un ballon de foot : voilà ce que j’ai emporté au Cameroun dans cette léproserie où j’arrive le 24 août 1967, près de Douala.  Pourtant,  j’ai sans doute été bien inspiré…  
