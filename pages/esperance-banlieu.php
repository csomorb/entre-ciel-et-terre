Trois diffétentes Rubriques:

- Qui sont ils?

   Espérance Banlieue est une école fondé pour favoriser le développement d’écoles indépendantes (hors contrat) de qualité, en plein cœur des banlieues. françaises.



- Leurs Idéaux

 La Fondation Espérance banlieues a pour mission de :

    favoriser l’accès de tous les enfants de banlieue à une instruction de qualité
    transmettre la connaissance et l’amour de la culture française





- Notre Missions avec Espérance Banlieue

nous allons nous associer à une classe de 5ème de la Cordée, école situé à Roubaix dans le nord de la France, qui est des villes la plus “pauvre” de France. L’objectif est de commencer à préparer notre voyage parmi eux pendant une semaine pour apprendre à connaître “notre” classe. Puis tout au long du voyage, nous nous efforcerons de rester en contact par le biais de plusieurs actions:
-    Toutes les deux semaines, nous appellerons la classe sur Skype pour tout simplement raconté nos aventures et nos mésaventures.
-    Les enfants devront nous transmettre un compte rendu des endroits que nous allons traverser. Ceci dans le but de véritablement découvrir les pays que ou nous allons passer
-    En retour, les enfants pourrons nous donner des “défis” que nous réaliserons comme par exemple: “Combien de joueur de foot les iraniens connaissent? “, “faire situer la France sur une carte à des enfants turques” ect
-    Nous allons essayer de faire en sorte que les enfants soient nos “Antennes relai” en France ou ils pourront envoyer une newsletter et même publier des posts Facebook des aventures que nous vivrons.










