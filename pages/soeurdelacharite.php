                <h2 class="subtitle">Leurs Idéaux</h2>
                <p>
                   Les Missionnaires de la Charité, outre les trois vœux de pauvreté, de chasteté et d’obéissance, 
                   prononcent un quatrième vœu : celui de se vouer au service des plus pauvres d’entre les pauvres, 
                   de ceux qui ne peuvent les dédommager de leur peine, dont on n’attend rien en retour. Mère Teresa 
                   limite volontairement le travail des sœurs à une catégorie de personnes, les plus marginales, défigurées 
                   ou rejetées par la société. C'est dans une volonté d'identification à Jésus abandonné par les siens, celui 
                   qui « s’est anéanti lui-même, en prenant la condition d’esclave » (Ph 2, 7).
                </p>
                <p>
                    Dans ce cadre, le vœu de pauvreté occupe une place centrale dans la vie et la spiritualité de la Congrégation :
                    « Nous essayons d’aider les novices à comprendre que la pauvreté, non seulement d’esprit, mais aussi de biens matériels,
                    est liberté. Une fois qu’elles ont compris ce qu’est la pauvreté et en quoi elle consiste, elles peuvent progresser
                    spirituellement moyennant la foi en Jésus et en l’eucharistie ».
                </p>
