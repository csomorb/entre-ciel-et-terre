Vivant depuis toujours en France, nous nous sommes peu à peu questionnés sur les façons de vivre des Hommes de différentes cultures. Qu’est-ce que cela veut dire être frères si on ne connaît rien de notre famille ? On a tant à apprendre de la diversité.
